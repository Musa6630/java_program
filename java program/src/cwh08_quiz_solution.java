public class cwh08_quiz_solution {
    public static void main(String[] args) {
        //Increment operators
        int a = 5;
        //int c = a++;
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a);

        //Decrement operators
        int b = 7;
        System.out.println(b--);
        System.out.println(--b);
        System.out.println(b--);
        System.out.println(b);
    }
}
